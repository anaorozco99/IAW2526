AND CO.CIFC = DI.CIFC AND UPPER(CIUDAD) = 'BARCELONA';

rem --2 Obtener el código de los coches de aquellos coches vendidos a clientes de Madrid.
SELECT CODCOCHE FROM VENTAS V, CLIENTES C WHERE INITCAP(C.CIUDAD)='Madrid' AND V.DNI=C.DNI;

rem --3 obtener nombre y apellido de los clientes que han adquirido algun coche del concesionario Dcar (Realizarlo con subconsulta) 
SELECT C.NOMBRE, C.APELLIDO FROM CLIENTES C, CONCESIONARIOS CO, VENTAS V WHERE C.DNI = V.DNI AND V.CIFC = CO.CIFC
AND CO.NOMBRE ='Dcar';

rem --4 obtener el nombre y la cantidad total de coche que ha distribuido cada concesionario (agrupamiento).
SELECT CO.NOMBRE, SUM(CANTIDAD) FROM CONCESIONARIOS CO, DISTRIBUCION D WHERE CO.CIFC = D.CIFC GROUP BY D.CIFC, CO.NOMBRE;

rem --5 Obtener el nombre de las marcas que tienen modelo gti
SELECT DISTINCT M.NOMBRE FROM MARCAS M, MARCO MC, COCHES C WHERE M.CIFM = MC.CIFM AND MC.CODCOCHE = C.CODCOCHE
AND MODELO='gti';

rem --6 Obtener los nombres de los coches que no tienen modelo gtd (usar operación de conjunto)
SELECT NOMBRE FROM COCHES MINUS SELECT NOMBRE FROM COCHES WHERE MODELO='gtd';

rem --7 Obtener el cif de todos los concesionarios que disponen de una cantidad de coches  comprendida entre 10 y 18 unidades (agrupamiento con having).
SELECT CIFC, SUM(CANTIDAD) FROM DISTRIBUCION GROUP BY CIFC HAVING SUM(CANTIDAD)>10 AND SUM(CANTIDAD)<18;

rem --8 Obtener dni, nombre y apellido de todos los clientes, mostrando el código de coche  que han comprado.
SELECT C.DNI, C.NOMBRE, C.APELLIDO, V.CODCOCHE FROM CLIENTES C, VENTAS V WHERE V.DNI = C.DNI ORDER BY C.DNI DESC;

