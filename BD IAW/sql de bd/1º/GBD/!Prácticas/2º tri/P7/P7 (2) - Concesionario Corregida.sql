SELECT C.DNI, C.NOMBRE, C.APELLIDO, V.CODCOCHE FROM CLIENTES C, VENTAS V WHERE V.DNI(+) = C.DNI ORDER BY C.DNI DESC;

rem --9 obtener el codcoche de los coches que han sido adquiridos por un cliente de Madrid en un concesionario de Madrid

SELECT CODCOCHE FROM VENTAS, CLIENTES, CONCESIONARIOS WHERE CLIENTES.CIUDAD='Madrid'
AND CONCESIONARIOS.CIFC = VENTAS.CIFC
AND CLIENTES.DNI = VENTAS.DNI
AND CONCESIONARIOS.CIUDAD='Madrid';

rem --10 Obtener los codche de los coches comprados en un concesionario de la misma ciudad que el cliente que lo compra

SELECT CODCOCHE FROM VENTAS, CLIENTES, CONCESIONARIOS CO WHERE CO.CIUDAD = CLIENTES.CIUDAD
AND CO.CIFC = VENTAS.CIFC
AND CLIENTES.DNI = VENTAS.DNI;

rem --11 Obtener todas las parejas de nombre de marcas que sean de la misma ciudad (Nota: se combina la tabla consigo mismo poniéndoles alias distinto (ejemplo: from tabla t1, tabla t2)
SELECT A.NOMBRE, B.NOMBRE 
FROM MARCAS A, MARCAS B 
WHERE A.CIUDAD = B.CIUDAD   -misma ciudad
AND A.NOMBRE > B.NOMBRE;    -para que no se repita

rem --12 Obtener las parejas de modelos de coches cuyo nombre es el mismo y cuya marca es de  Bilbao

SELECT A.MODELO, B.MODELO FROM COCHES A, COCHES B, MARCO, MARCAS WHERE A.NOMBRE = B.NOMBRE
AND A.MODELO > B.MODELO AND A.CODCOCHE = MARCO.CODCOCHE
AND MARCO.CIFM = MARCAS.CIFM AND MARCAS.CIUDAD='Bilbao';

